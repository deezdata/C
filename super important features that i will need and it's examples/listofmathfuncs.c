#include <stdio.h>
#include <stdlib.h>

int main()
{

//sqrt()																				          		                  Calculates the square root of a floating-point value
printf("%f\n", sqrt(2) );
//pow()	  																							    	                  Returns the result of a floating-point value raised to a certain power
printf("%f\n", pow(2, 3) );
//abs()	                                                                        Returns the absolute value (positive value) of an integer(модуль)
printf("%d\n", abs(-5000) );
//floor()	                                                             					Rounds up a floating-point value to the next whole number(nonfractional) value
printf("%f\n", floor(13.54) );
//ceil()                                                                        Rounds down a floating-point value to the next wholenumber
printf("%f\n", ceil(13.54) );


return 0;
}
